END;
$function$;

-- Generates an 8-char invite code. Caller's company is the tenant.
CREATE OR REPLACE FUNCTION public.create_invite(p_role text)
RETURNS text
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
DECLARE
  v_company_id uuid;
  v_code       text;
BEGIN
  SELECT company_id INTO v_company_id FROM profiles WHERE id = auth.uid();
  IF NOT FOUND THEN RAISE EXCEPTION 'Profile not found'; END IF;
  v_code := upper(substring(replace(gen_random_uuid()::text, '-', ''), 1, 8));
  INSERT INTO invites (company_id, role, code, created_by)
  VALUES (v_company_id, p_role, v_code, auth.uid());
  RETURN v_code;
END;
$function$;

-- Consumes an invite code; sets up the new user's profile.
CREATE OR REPLACE FUNCTION public.use_invite(p_code text, p_full_name text)
RETURNS text
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
DECLARE
  v_invite  invites%ROWTYPE;
  v_user_id uuid := auth.uid();
BEGIN
  SELECT * INTO v_invite FROM invites
  WHERE code = upper(trim(p_code)) AND used_at IS NULL AND expires_at > now();
  IF NOT FOUND THEN RAISE EXCEPTION 'Invalid or expired invite code'; END IF;

  INSERT INTO profiles (id, email, full_name, role, is_active, company_id, created_at)
  SELECT v_user_id, email, p_full_name, v_invite.role, true, v_invite.company_id, now()
  FROM auth.users WHERE id = v_user_id
  ON CONFLICT (id) DO UPDATE SET
    company_id = v_invite.company_id,
    role       = v_invite.role,
    full_name  = p_full_name,
    is_active  = true;

  UPDATE invites SET used_by = v_user_id, used_at = now() WHERE id = v_invite.id;
  RETURN v_invite.role;
END;
$function$;

-- Admin gate used by token-mint + AI-config functions. Note: checks
-- against 'officeAdmin' (camelCase) which is INCONSISTENT with iOS
-- UserRole raw values (snake_case 'office_admin'). See "KNOWN ISSUES"
-- header above. Office admin users currently fail this check.
CREATE OR REPLACE FUNCTION public._require_company_admin()
RETURNS TABLE(user_id uuid, company_id uuid, role text)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
DECLARE
  v_user_id    uuid := auth.uid();
  v_company_id uuid;
  v_role       text;
BEGIN
  IF v_user_id IS NULL THEN
    RAISE EXCEPTION 'Not authenticated' USING ERRCODE = '42501';
  END IF;
  SELECT p.company_id, p.role INTO v_company_id, v_role
  FROM public.profiles p
  WHERE p.id = v_user_id;
  IF v_company_id IS NULL THEN
    RAISE EXCEPTION 'No profile / company for caller' USING ERRCODE = '42501';
  END IF;
  IF v_role NOT IN ('owner', 'executive', 'manager', 'officeAdmin') THEN
    RAISE EXCEPTION 'Admin role required' USING ERRCODE = '42501';
  END IF;
  RETURN QUERY SELECT v_user_id, v_company_id, v_role;
END $function$;

