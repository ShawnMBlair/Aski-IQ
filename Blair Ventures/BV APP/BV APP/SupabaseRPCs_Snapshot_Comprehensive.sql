           updated_at          = now()
     where id = v_company;
  else
    update companies
       set ai_prompt_overrides = ai_prompt_overrides
                                  || jsonb_build_object(p_surface, p_prompt),
           updated_at          = now()
     where id = v_company;
  end if;
end;
$function$;

