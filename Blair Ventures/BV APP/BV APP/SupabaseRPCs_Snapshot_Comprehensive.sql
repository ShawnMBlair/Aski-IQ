-- AI usage rollup. Inserts both a per-(company,user) row AND a
-- per-company rollup row. ON CONFLICT updates the totals.
CREATE OR REPLACE FUNCTION public.record_ai_usage(
  p_company_id uuid, p_user_id uuid, p_model text,
  p_input_tokens bigint, p_output_tokens bigint, p_cost_cents bigint
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
DECLARE
  v_today date := current_date;
BEGIN
  INSERT INTO public.ai_usage_daily (
    company_id, user_id, usage_date, request_count,
    input_tokens, output_tokens, cost_cents,
    model_breakdown, updated_at
  ) VALUES (
    p_company_id, p_user_id, v_today, 1,
    p_input_tokens, p_output_tokens, p_cost_cents,
    jsonb_build_object(p_model, jsonb_build_object(
      'requests', 1,
      'input_tokens', p_input_tokens,
      'output_tokens', p_output_tokens,
      'cost_cents', p_cost_cents
    )),
    now()
  )
  ON CONFLICT (company_id, coalesce(user_id, '00000000-0000-0000-0000-000000000000'::uuid), usage_date)
  DO UPDATE SET
    request_count   = public.ai_usage_daily.request_count + 1,
    input_tokens    = public.ai_usage_daily.input_tokens  + EXCLUDED.input_tokens,
    output_tokens   = public.ai_usage_daily.output_tokens + EXCLUDED.output_tokens,
    cost_cents      = public.ai_usage_daily.cost_cents    + EXCLUDED.cost_cents,
    model_breakdown = public.ai_usage_daily.model_breakdown
                      || jsonb_build_object(p_model, jsonb_build_object(
                        'requests',
                          coalesce((public.ai_usage_daily.model_breakdown->p_model->>'requests')::int, 0) + 1,
                        'input_tokens',
                          coalesce((public.ai_usage_daily.model_breakdown->p_model->>'input_tokens')::bigint, 0) + EXCLUDED.input_tokens,
                        'output_tokens',
                          coalesce((public.ai_usage_daily.model_breakdown->p_model->>'output_tokens')::bigint, 0) + EXCLUDED.output_tokens,
                        'cost_cents',
                          coalesce((public.ai_usage_daily.model_breakdown->p_model->>'cost_cents')::bigint, 0) + EXCLUDED.cost_cents
                      )),
    updated_at = now();

  INSERT INTO public.ai_usage_daily (
    company_id, user_id, usage_date, request_count,
    input_tokens, output_tokens, cost_cents, updated_at
  ) VALUES (
    p_company_id, NULL, v_today, 1,
    p_input_tokens, p_output_tokens, p_cost_cents, now()
  )
  ON CONFLICT (company_id, coalesce(user_id, '00000000-0000-0000-0000-000000000000'::uuid), usage_date)
  DO UPDATE SET
    request_count = public.ai_usage_daily.request_count + 1,
    input_tokens  = public.ai_usage_daily.input_tokens  + EXCLUDED.input_tokens,
    output_tokens = public.ai_usage_daily.output_tokens + EXCLUDED.output_tokens,
    cost_cents    = public.ai_usage_daily.cost_cents    + EXCLUDED.cost_cents,
    updated_at    = now();
END $function$;


