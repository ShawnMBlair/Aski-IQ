-- BYOK Anthropic key — stored in Supabase Vault, fetched via this proxy.
-- Caller is the Edge Function relaying iOS requests to Anthropic.
CREATE OR REPLACE FUNCTION public.get_company_ai_key_for_proxy(p_company_id uuid)
RETURNS text
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public', 'vault'
AS $function$
DECLARE
  v_secret_id uuid;
  v_value     text;
BEGIN
  SELECT anthropic_api_key_vault_id INTO v_secret_id
  FROM public.companies WHERE id = p_company_id;
  IF v_secret_id IS NULL THEN RETURN NULL; END IF;

  SELECT decrypted_secret INTO v_value
  FROM vault.decrypted_secrets WHERE id = v_secret_id;

  RETURN v_value;
END $function$;

