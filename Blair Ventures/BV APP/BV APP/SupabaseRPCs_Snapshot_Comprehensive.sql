-- Stripe webhook lands here. Idempotent on payment_intent_id.
-- Recomputes total_paid + balance_due from the payments JSONB array
-- so we don't drift if a prior write was incomplete.
CREATE OR REPLACE FUNCTION public.record_invoice_stripe_payment(
  p_invoice_id uuid, p_payment_intent_id text,
  p_amount numeric, p_currency text DEFAULT 'USD'::text
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
declare
  v_invoice    invoices%rowtype;
  v_payments   jsonb;
  v_existing   jsonb;
  v_total_paid numeric := 0;
  v_balance    numeric;
  v_new_status text;
begin
  select * into v_invoice from invoices where id = p_invoice_id;
  if not found then
    raise exception 'Invoice % not found', p_invoice_id;
  end if;

  v_existing := coalesce(v_invoice.payments, '[]'::jsonb);
  if exists (
    select 1
    from jsonb_array_elements(v_existing) e
    where e->>'paymentIntentID' = p_payment_intent_id
       or e->>'externalReference' = p_payment_intent_id
  ) then
    return;
  end if;

  v_payments := v_existing || jsonb_build_array(jsonb_build_object(
    'id',                gen_random_uuid()::text,
    'amount',            p_amount,
    'paymentDate',       to_jsonb(now()),
    'method',            'stripe',
    'externalReference', p_payment_intent_id,
    'paymentIntentID',   p_payment_intent_id,
    'currency',          coalesce(p_currency, 'USD'),
    'notes',             case when p_amount < 0 then 'Stripe refund' else 'Stripe payment' end,
    'recordedBy',        'stripe-webhook',
    'recordedAt',        to_jsonb(now())
  ));

  select coalesce(sum((e->>'amount')::numeric), 0)
    into v_total_paid
    from jsonb_array_elements(v_payments) e;

  v_balance := coalesce(v_invoice.total, 0) - v_total_paid;

  if v_balance <= 0 then
    v_new_status := 'paid';
  elsif v_total_paid > 0 then
    v_new_status := 'partial';
  else
    if v_invoice.status in ('paid', 'partial') then
      v_new_status := 'sent';
    else
      v_new_status := v_invoice.status;
    end if;
  end if;

  update invoices
     set payments    = v_payments,
         total_paid  = v_total_paid,
         balance_due = v_balance,
         status      = v_new_status,
         paid_at     = case when v_new_status = 'paid' then now() else null end,
         updated_at  = now()
   where id = p_invoice_id;
end;
$function$;


