-- Sets per-company Anthropic API key. Empty input revokes.
-- Goes through Vault for at-rest encryption.
CREATE OR REPLACE FUNCTION public.set_company_ai_key(p_key text)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
DECLARE
  v_admin           record;
  v_clean           text;
  v_existing_id     uuid;
  v_company_name    text;
BEGIN
  SELECT * INTO v_admin FROM public._require_company_admin();
  v_clean := btrim(coalesce(p_key, ''));

  SELECT anthropic_api_key_vault_id, name
    INTO v_existing_id, v_company_name
  FROM public.companies WHERE id = v_admin.company_id;

  IF v_clean = '' THEN
    IF v_existing_id IS NOT NULL THEN
      DELETE FROM vault.secrets WHERE id = v_existing_id;
    END IF;
    UPDATE public.companies
       SET anthropic_api_key_vault_id     = NULL,
           anthropic_api_key_updated_at   = now(),
           anthropic_api_key_updated_by   = v_admin.user_id
     WHERE id = v_admin.company_id;
    RETURN;
  END IF;

  IF v_clean NOT LIKE 'sk-ant-%' THEN
    RAISE EXCEPTION 'Key does not look like an Anthropic API key (expected prefix sk-ant-)'
      USING ERRCODE = '22023';
  END IF;

  IF v_existing_id IS NOT NULL THEN
    PERFORM vault.update_secret(v_existing_id, v_clean);
    UPDATE public.companies
       SET anthropic_api_key_updated_at = now(),
           anthropic_api_key_updated_by = v_admin.user_id
     WHERE id = v_admin.company_id;
  ELSE
    UPDATE public.companies
       SET anthropic_api_key_vault_id = vault.create_secret(
              v_clean,
              'anthropic_api_key_' || v_admin.company_id::text,
              'Per-company Anthropic API key (BYOK) for ' || v_company_name
            ),
           anthropic_api_key_updated_at = now(),
           anthropic_api_key_updated_by = v_admin.user_id
     WHERE id = v_admin.company_id;
  END IF;
END $function$;

