-- Trigger that fires when auth.users gets a new row (via Supabase
-- signup). Creates the tenant company + the user's profile.
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public', 'auth', 'pg_temp'
AS $function$
DECLARE
    new_company_id uuid;
    company_name   text;
BEGIN
    company_name := COALESCE(
        NEW.raw_user_meta_data->>'company_name',
        'My Company'
    );

    INSERT INTO companies (name)
    VALUES (company_name)
    RETURNING id INTO new_company_id;

    INSERT INTO profiles (id, email, full_name, company_id)
    VALUES (
        NEW.id,
        NEW.email,
        COALESCE(NEW.raw_user_meta_data->>'full_name', ''),
        new_company_id
    )
    ON CONFLICT (id) DO NOTHING;

    RETURN NEW;
END;
$function$;

-- Manual setup path used by login when handle_new_user didn't fire.
CREATE OR REPLACE FUNCTION public.setup_new_user(p_full_name text, p_company_name text)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO 'public'
AS $function$
DECLARE
  v_user_id   uuid := auth.uid();
  v_company_id uuid;
BEGIN
  INSERT INTO companies (name, created_at)
  VALUES (p_company_name, now())
  RETURNING id INTO v_company_id;

  INSERT INTO profiles (id, email, full_name, role, is_active, company_id, created_at)
